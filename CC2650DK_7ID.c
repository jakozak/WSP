/*
 *  ========================= IO driver initialization =========================
 *  From main, PIN_init(BoardGpioInitTable) should be called to setup safe
 *  settings for this board.
 *  When a pin is allocated and then de-allocated, it will revert to the state
 *  configured in this table.
*/

/* Place into subsections to allow the TI linker to remove items properly */
#if defined(__TI_COMPILER_VERSION__)
#pragma DATA_SECTION(BoardGpioInitTable, ".const:BoardGpioInitTable")
#pragma DATA_SECTION(PINCC26XX_hwAttrs, ".const:PINCC26XX_hwAttrs")
#endif

const PIN_Config BoardGpioInitTable[] = {

    Board_LED 		 | PIN_GPIO_OUTPUT_EN | PIN_GPIO_LOW    | PIN_PUSHPULL | PIN_DRVSTR_MAX,     /* LED initially off               */
    Board_BUTTON	 | PIN_INPUT_EN 	  | PIN_PULLUP		| PIN_HYSTERESIS,                             /* Button is active low            */
    Board_UART_TX    | PIN_GPIO_OUTPUT_EN | PIN_GPIO_LOW    | PIN_PUSHPULL,                     /* UART TX pin at inactive level   */
	Board_XBEE_SLP   | PIN_GPIO_OUTPUT_EN | PIN_GPIO_LOW    | PIN_PUSHPULL | PIN_DRVSTR_MIN,
	Board_3V3_EN  	 | PIN_GPIO_OUTPUT_EN | PIN_GPIO_LOW    | PIN_PUSHPULL | PIN_DRVSTR_MIN,
	Board_VTRANS_EN  | PIN_GPIO_OUTPUT_EN | PIN_GPIO_LOW    | PIN_PUSHPULL | PIN_DRVSTR_MIN,
	Board_SLEEP_IND	 | PIN_INPUT_EN 	  | PIN_NOPULL		| PIN_HYSTERESIS,
	Board_ASSCT		 | PIN_INPUT_EN 	  | PIN_NOPULL		| PIN_HYSTERESIS,
	PIN_TERMINATE                                                                               /* Terminate list                  */
};

const PINCC26XX_HWAttrs PINCC26XX_hwAttrs = {
    .intPriority = ~0,
    .swiPriority = 0
};
